def solution(n, a, b):
    answer = 0

    # 라운드가 올라갈때마다 반씩 줄어듦
    # 4,5 가 만나려면 3라운드 필요 12 34 56 78 / 24 57 / 4 5
    # 4//2 , 5//2 == 2 x
    # (4+1)//2 , (5+1)//2 == 2,3
    # (2+1)//2 , (3+1)//2 == 1,2
    # (1+1)//2 , (2+1)//2 == 1,1 < 만나는 지점
    while a != b:
        a = (a + 1) // 2
        b = (b + 1) // 2
        print(a, b)
        answer += 1

    return answer